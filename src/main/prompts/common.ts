// ── 动态字数区间 ──

export interface WordCountRange {
  min: number
  max: number
  guidance: string
}

/**
 * 根据转录文本长度计算建议输出字数区间。
 * 硬下界 600 字，硬上界 8000 字。
 */
export function getWordCountRange(transcriptLength: number): WordCountRange {
  if (transcriptLength < 5000) {
    return {
      min: 800,
      max: 1500,
      guidance: '内容较短，建议精炼输出，聚焦核心要点。'
    }
  }
  if (transcriptLength <= 15000) {
    return {
      min: 1500,
      max: 3500,
      guidance: '素材量适中，可展开分析但避免注水。'
    }
  }
  return {
    min: 3000,
    max: 8000,
    guidance: '素材丰富，请充分展开深度分析，覆盖各章节。'
  }
}

/**
 * 生成注入到 article prompt 中的字数指导文本
 */
export function formatWordCountGuidance(transcriptLength: number, segmentCount: number): string {
  const range = getWordCountRange(transcriptLength)
  return [
    `原文约 ${transcriptLength} 字符，${segmentCount} 个段落。`,
    `建议输出 ${range.min}–${range.max} 中文字。${range.guidance}`,
    '请自行判断内容密度，灵活调整文章长度，但不少于 600 字且不超过 8000 字。'
  ].join(' ')
}
